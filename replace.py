#Replace Method: This method replace one part of substring with another substring.
#syntax: VariableName.replace(arg1,arg2,arg3)
#first 2 arguments are mandatory,return type is string.
s = 'we are developers'
print(s.replace('a',''))
print(s)
print(s.replace('e','@'))
print(s.replace('r','#',1))

